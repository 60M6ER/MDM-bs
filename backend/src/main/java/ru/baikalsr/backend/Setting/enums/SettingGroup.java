package ru.baikalsr.backend.Setting.enums;

/**
 * Группы настроек приложения.
 * <p>
 * Каждая группа хранится в таблице {@code settings} в виде отдельной записи,
 * где поле {@code value} содержит JSON-объект со структурой,
 * соответствующей конкретной группе.
 * <p>
 * Сейчас активна только {@link #AUTH_SETTINGS} — настройки авторизации.
 * Остальные группы зарезервированы для будущих модулей.
 */
public enum SettingGroup {


    //APP_TITLES,      // Заголовки сайта и страниц

    /**
     * Настройки централизованной авторизации.
     * <p>
     * Содержит параметры подключения к LDAP/AD или OIDC:
     * <ul>
     *   <li>provider — тип провайдера (ad | ldap | oidc)</li>
     *   <li>ad / ldap / oidc — соответствующие секции с параметрами подключения</li>
     * </ul>
     */
    AUTH_SETTINGS,   // Настройки авторизации (LDAP, AD, OIDC)
    //MAIL_SETTINGS,   // Настройки SMTP (если планируется рассылка)
    //UI_FEATURES,     // Управление фичами интерфейса (опционально)
    //SYSTEM_LIMITS    // Лимиты, таймауты и др. системные параметры
}
