package ru.baikalsr.backend.Device.enums;

public enum DeviceStatus {

    /** Устройство активно в эксплуатации */
    ACTIVE,

    /** Временно недоступно (сломано, в сервисе, диагностика) */
    REPAIR,

    /** Устройство потеряно, пропало, не выходит на связь долгое время */
    LOST,

    /** Устройство снято с эксплуатации, списано; хранится как архив */
    RETIRED,

    /** Зарезервировано, ждёт передачи сотруднику */
    RESERVED,

    /** Новое устройство, зарегистрировано, но ещё не введено в эксплуатацию */
    NEW
}
